package practice;

public class MonthSwitchCase {
	public static void main(String[] args) {
		String month ="May";
		switch (month)
		{
		case "Jan" : System.out.println("26th jan republic day");break;
		case "Feb" : System.out.println("14th feb valentine day");break;
		case "March" : System.out.println("Gudipadawa");break;
		case "April" : System.out.println("Ramnavami");break;
		case "May" : System.out.println("Maharashtra day");break;
		case "June" : System.out.println("Vatpornima");break;
		case "July" : System.out.println("Bail pola");break;
		case "Agust" : System.out.println("Indepedance day");break;
		case "sept" : System.out.println(" Navaratri");break;
		case "oct" : System.out.println("Mahatma Gandhi Jayanti");break;
		case "nov" : System.out.println("Diwali");break;
		case "Dec" : System.out.println("25 th dec Christmas");break;
		}
}}
